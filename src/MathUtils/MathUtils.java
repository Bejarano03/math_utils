package MathUtils;

public class MathUtils {
    public static int Add(int numA, int numB) {
        return numA + numB;
    }

    public static int Subtract(int numA, int numB) {
        return numA - numB;
    }

    public static double Multiply(double numA, double numB) {
        return numA * numB;
    }

    public static double Divide(double numA, double numB) {
        return numA / numB;
    }
}
